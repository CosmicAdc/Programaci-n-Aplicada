public static void cambiamos(int c, int[][] pk){
    int val0=pk[0][c];
    int val1=pk[1][c];
    int val2=pk[2][c];
    int val3=pk[3][c];
    
    if (c==0){
        jLabel2.setIcon(imagen(val0));
        jLabel5.setIcon(imagen(val1));
        jLabel8.setIcon(imagen(val2));
        jLabel11.setIcon(imagen(val3));
    }
    if  (c==1){
       jLabel3.setIcon(imagen(val0));
        jLabel6.setIcon(imagen(val1));
        jLabel9.setIcon(imagen(val2));
        jLabel12.setIcon(imagen(val3));
    }
    if (c==2){
        jLabel4.setIcon(imagen(val0));
        jLabel7.setIcon(imagen(val1));
        jLabel10.setIcon(imagen(val2));
        jLabel13.setIcon(imagen(val3));
    }
}
public static Icon imagen(int llega){
    if (llega==0){
        return icono0;
    }
    if (llega==1){
      return icono1;
    }
    if (llega==2){
        return icono2;
    }
    if (llega==3){
      return icono3;
    }
    return null;
}

