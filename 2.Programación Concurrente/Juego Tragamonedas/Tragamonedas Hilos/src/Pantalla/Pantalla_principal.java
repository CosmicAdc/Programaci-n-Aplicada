public void interrumpir(ActionEvent evt){ 

    try{
        if(evt.getSource().equals(P1))
        {
            h1.interrupt();
            Col1=false;
        }
        if (evt.getSource().equals(P2))
        {
            h2.interrupt();
            Col2=false;
        }
        if (evt.getSource().equals(P3))
        {
            h3.interrupt();
            Col3=false;
        }
        if (evt.getSource().equals(PT))
        {
            h1.interrupt();
            h2.interrupt();
            h3.interrupt();
            Col1=false;
            Col2=false;
            Col3=false;
        }
        }catch(Exception ex) {
                
       }
    }

